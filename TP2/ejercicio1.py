# Calculadora básica
# Descripción: Crear una calculadora que realice las operaciones básicas (+, -, *, /) entre dos
# números ingresados por el usuario.

num1 = float(input("Ingrese el primer número: "))
num2 = float(input("Ingrese el segundo número: "))

print(f"La suma de {num1} y {num2} es {num1 + num2}")
print(f"La resta de {num1} y {num2} es {num1 - num2}")
print(f"La multiplicación de {num1} y {num2} es {num1 * num2}")
print(f"La divisió de {num1} y {num2} es {num1 / num2}")
mi_lista = [1, 2, 3, 4, 5]
mi_lista2 = ["manzana", "pera", "uva"]  

#len: devuelve el número de elementos de la lista
len(mi_lista)
len(mi_lista2)


#append: agrega un elemento al final de la lista
mi_lista2.append("banana")


#insert: inserta un elemento en una posición específica
mi_lista2.insert(0, "kiwi")


#extend: agrega los elementos de una lista a otra lista
mi_lista2.extend(["kiwi", "manzana"])


#pop: elimina un elemento de la lista por indice
mi_lista2.pop(0)


#remove: elimina un elemento de la lista por valor
mi_lista2.remove("kiwi")


#sort: ordena los elementos de la lista en orden ascendente
mi_lista.sort()
mi_lista.reverse()


#clear: elimina todos los elementos de la lista
mi_lista2.clear()






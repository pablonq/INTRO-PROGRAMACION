# Incremento y Decremento: Declara una variable `x` con un valor inicial de 5. Usa operadores
# de asignación compuesta (`+=` y `-=`) para incrementarla en 3 y luego decrementarla en 2.
# Muestra el valor final de `x`.
# - Operadores utilizados: `+=`, `-=`


x = float(input("Ingrese un número: "))

x +=3

x -=2

print(f"El resultado es: {x}")



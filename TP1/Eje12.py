# Descuento en Compra (Expresión Booleana): Pide al usuario el precio de un producto y
# evalúa si tiene derecho a un descuento del 10% si el precio es mayor a 100. Muestra el resultado
# de la expresión `precio > 100`.
# - Operadores utilizados: `>` 

precio = float(input("Ingrese el precio del producto: "))

print(f"El precio {precio} es mayor a 100?: {precio > 100}")

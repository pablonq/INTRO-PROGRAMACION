# Comparación de Números: Pide al usuario dos números y muestra los resultados de las
# siguientes comparaciones:
# - ¿El primer número es mayor que el segundo? (`>`).
# - ¿El primer número es igual al segundo? (`==`).
# - ¿El primer número es menor que el segundo? (`<`).
# - Operadores utilizados: `>`, `<`, `==`

num1 = float(input("Ingrese un número: "))
num2 = float(input("Ingrese otro número: "))

print(num1 > num2)
print(num1 == num2)
print(num1 < num2)
